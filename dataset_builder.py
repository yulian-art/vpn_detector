#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Build ML-ready tabular datasets from features.jsonl and labels.csv/xlsx.

Input:
  - features.jsonl generated by vpn_detector.feature_extractor
  - labels.csv/xlsx generated by build_label_table.py or manually curated manifest

Output:
  - dataset.csv or dataset.parquet, one row per pcap/sample
  - numeric feature columns + labels + metadata

Design goals:
  1. Avoid data leakage by preserving capture_id/source fields for group split.
  2. Separate identity features (IP/SNI/domain/port exact matches) from behavior features.
  3. Provide stable feature names for RF/LightGBM/XGBoost training.
"""

from __future__ import annotations

import argparse
import json
import logging
import math
import re
from pathlib import Path
from typing import Any, Dict, Iterable, List, Optional, Tuple

import pandas as pd

from .config import BLOCK_SIZES, DOMAIN_KEYWORDS, RISK_TLDS, SPECIAL_PORTS, STANDARD_TLS_PORTS
from .identity import make_capture_id, make_sample_id, make_split_group
from .utils import extract_ja4_cipher_count, flatten_pairs

logger = logging.getLogger(__name__)

META_COLUMNS = {
    "sample_id", "capture_id", "source_archive", "pcap_member", "file_name",
    "label_binary", "label_protocol", "label_tool", "label_family", "vpn_family", "scenario",
    "is_vpn", "need_review", "review_status", "label_status", "label_confidence",
    "sample_weight", "split_group", "feature_version", "label_version", "note",
    "size_bytes", "feature_set_hint", "device_id", "network_id", "time_period",
    "source_path", "label_score", "positive_votes", "negative_votes", "conflict_reasons",
    "evidence_json",
}

IDENTITY_PREFIXES = (
    "id_", "domain_kw_", "sni_", "dns_", "ip_", "asn_", "port_exact_"
)
IDENTITY_EXACT_COLUMNS = {
    "file_size_bytes",
    "source_archive",
    "pcap_member",
    "file_name",
    "top_endpoint",
}
BEHAVIOR_PREFIXES = (
    "flow_", "pkt_", "iat_", "bytes_", "ratio_", "block_", "mtu_", "duration_",
    "endpoint_", "payload_", "tcp_", "udp_", "quic_", "wpad_", "random_local_",
    "single_flow_", "long_flow_", "dominant_", "top_endpoint_ratio"
)
TLS_PREFIXES = ("tls_", "ja3_", "ja4_", "cipher_", "alpn_")
DNS_PREFIXES = ("dns_", "domain_", "risk_tld_", "random_local_", "wpad_")
PORT_PREFIXES = ("port_", "port_exact_", "special_port_")
SAMPLE_WEIGHT_BY_CONFIDENCE = {
    "strong": 1.0,
    "medium": 0.7,
    "weak": 0.35,
    "unlabeled": 0.0,
}


def load_jsonl(path: Path) -> Iterable[Dict[str, Any]]:
    with path.open("r", encoding="utf-8") as fh:
        for line in fh:
            line = line.strip()
            if line:
                yield json.loads(line)


def read_labels(path: Optional[Path]) -> pd.DataFrame:
    if path is None:
        return pd.DataFrame()
    if not path.exists():
        raise FileNotFoundError(f"label/manifest file not found: {path}")
    if path.suffix.lower() == ".parquet":
        df = pd.read_parquet(path)
    elif path.suffix.lower() in {".xlsx", ".xls"}:
        df = pd.read_excel(path)
    else:
        df = pd.read_csv(path, encoding="utf-8-sig")
    return normalize_labels(df)


def normalize_labels(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()
    rename_map = {
        "is_vpn": "label_binary",
        "vpn_family": "label_tool",
        "family": "label_tool",
        "tool": "label_tool",
        "protocol": "label_protocol",
        "binary": "label_binary",
    }
    for src, dst in rename_map.items():
        if src in df.columns and dst not in df.columns:
            df[dst] = df[src]

    if "label_binary" in df.columns:
        df["label_binary"] = df["label_binary"].map(_normalize_binary_label)
    if "label_tool" not in df.columns and "vpn_family" in df.columns:
        df["label_tool"] = df["vpn_family"]
    if "label_protocol" not in df.columns:
        df["label_protocol"] = "unknown_protocol"
    if "label_confidence" not in df.columns:
        df["label_confidence"] = df["label_binary"].map(lambda x: "unlabeled" if pd.isna(x) or str(x).strip() == "" else "medium") if "label_binary" in df.columns else "unlabeled"
    df["label_confidence"] = df["label_confidence"].map(normalize_confidence)
    if "label_family" not in df.columns:
        df["label_family"] = df.get("vpn_family", df.get("label_tool", ""))
    if "capture_id" not in df.columns:
        # capture_id is intentionally coarse; user should later replace with true capture grouping.
        df["capture_id"] = df.apply(
            lambda row: make_capture_id(row.get("source_archive", ""), row.get("pcap_member", ""), row.get("file_name", "")), axis=1
        )
    if "split_group" not in df.columns:
        df["split_group"] = df.apply(
            lambda row: make_split_group(row.get("source_archive", ""), row.get("pcap_member", ""), row.get("file_name", ""), row.get("capture_id")), axis=1
        )

    for col in ["file_name", "pcap_member", "source_archive"]:
        if col in df.columns:
            df[col] = df[col].astype(str)
    return df


def _normalize_binary_label(x: Any) -> Optional[int]:
    s = str(x).strip().lower()
    if s in {"", "nan", "none", "null", "unknown", "unlabeled"}:
        return None
    if s in {"1", "true", "vpn", "yes", "y", "positive"}:
        return 1
    if s in {"0", "false", "nonvpn", "non-vpn", "normal", "no", "n", "negative"}:
        return 0
    try:
        numeric = int(float(s))
        return numeric if numeric in {0, 1} else None
    except Exception:
        return None


def normalize_confidence(x: Any) -> str:
    s = str(x or "").strip().lower()
    aliases = {"high": "strong", "verified": "strong", "manual": "strong", "unknown": "unlabeled", "": "unlabeled", "nan": "unlabeled"}
    s = aliases.get(s, s)
    return s if s in SAMPLE_WEIGHT_BY_CONFIDENCE else "unlabeled"


def sample_weight_for_confidence(confidence: Any) -> float:
    return SAMPLE_WEIGHT_BY_CONFIDENCE[normalize_confidence(confidence)]


def merge_label(row: Dict[str, Any], labels: pd.DataFrame) -> Dict[str, Any]:
    if labels.empty:
        # Weak fallback from filenames only. User should manually curate labels for real experiments.
        return {
            "label_binary": None,
            "label_protocol": "unknown_protocol",
            "label_tool": "unknown_tool",
            "label_family": "",
            "label_confidence": "unlabeled",
            "sample_weight": 0.0,
            "capture_id": row["sample_id"],
            "split_group": row["sample_id"],
            "scenario": "unknown",
            "need_review": "yes",
        }

    candidates = []
    for col in ["pcap_member", "file_name"]:
        if col in labels.columns and row.get(col):
            val = str(row[col])
            hit = labels[labels[col].astype(str) == val]
            if not hit.empty:
                candidates.append(hit.iloc[0])
            # fallback: basename match for pcap_member paths
            if col == "pcap_member":
                hit2 = labels[labels[col].astype(str).map(lambda x: Path(x).name) == Path(val).name]
                if not hit2.empty:
                    candidates.append(hit2.iloc[0])
    if not candidates and "file_name" in labels.columns:
        hit3 = labels[labels["file_name"].astype(str) == str(row.get("file_name", ""))]
        if not hit3.empty:
            candidates.append(hit3.iloc[0])

    if not candidates:
        return {
            "label_binary": None,
            "label_protocol": "unknown_protocol",
            "label_tool": "unknown_tool",
            "label_family": "",
            "label_confidence": "unlabeled",
            "sample_weight": 0.0,
            "capture_id": row["sample_id"],
            "split_group": row["sample_id"],
            "scenario": "unknown",
            "need_review": "yes",
        }

    lab = candidates[0].to_dict()
    confidence = normalize_confidence(lab.get("label_confidence", "medium"))
    return {
        "label_binary": lab.get("label_binary"),
        "label_protocol": str(lab.get("label_protocol", "unknown_protocol")),
        "label_tool": str(lab.get("label_tool", lab.get("vpn_family", "unknown_tool"))),
        "label_family": str(lab.get("label_family", lab.get("vpn_family", ""))),
        "label_confidence": confidence,
        "sample_weight": sample_weight_for_confidence(confidence),
        "capture_id": str(lab.get("capture_id", row["sample_id"])),
        "split_group": str(lab.get("split_group", lab.get("capture_id", row["sample_id"]))),
        "scenario": str(lab.get("scenario", "unknown")),
        "need_review": str(lab.get("need_review", "")),
        "review_status": str(lab.get("review_status", "")),
        "label_status": str(lab.get("label_status", "")),
        "note": str(lab.get("note", "")),
    }


def pairs_to_names(pairs: Any) -> List[str]:
    return [str(k).lower() for k, _ in flatten_pairs(pairs)]


def first_pair_value(pairs: Any) -> Tuple[str, int]:
    vals = flatten_pairs(pairs)
    if not vals:
        return "", 0
    return vals[0]


def add_numeric(out: Dict[str, Any], key: str, value: Any) -> None:
    if isinstance(value, bool):
        out[key] = int(value)
        return
    if isinstance(value, (int, float)):
        if isinstance(value, float) and (math.isinf(value) or math.isnan(value)):
            out[key] = 0.0
        else:
            out[key] = value
        return
    try:
        out[key] = float(value)
    except Exception:
        pass


def flatten_feature_record(rec: Dict[str, Any]) -> Dict[str, Any]:
    f = rec.get("file_feature", {}) or {}
    flows = rec.get("top_flows", []) or []

    row: Dict[str, Any] = {
        "source_archive": str(f.get("source_archive", "")),
        "pcap_member": str(f.get("pcap_member", "")),
        "file_name": str(f.get("file_name", "")),
    }
    row["sample_id"] = str(f.get("sample_id") or make_sample_id(row["source_archive"], row["pcap_member"], row["file_name"], f.get("file_size_bytes", "")))

    # Direct numeric file-level features.
    for key, val in f.items():
        if key in {"source_archive", "pcap_member", "file_name", "extract_error"}:
            continue
        if isinstance(val, (dict, list, tuple)):
            continue
        add_numeric(row, key, val)

    # Prefix aliases for cleaner feature grouping.
    alias = {
        "total_packets": "pkt_total_packets",
        "tcp_packets": "pkt_tcp_packets",
        "udp_packets": "pkt_udp_packets",
        "top_endpoint_ratio": "endpoint_top_ratio",
        "single_flow_dominance": "flow_single_dominance",
        "max_flow_duration": "duration_max_flow",
        "max_flow_iat_under_1ms_ratio": "iat_max_flow_under_1ms_ratio",
        "mtu_fill_ratio": "mtu_file_fill_ratio",
        "dominant_payload_size": "payload_dominant_size",
        "dominant_payload_ratio": "payload_dominant_ratio",
        "tls_clienthello_count": "tls_clienthello_count",
        "alpn_missing_ratio": "alpn_missing_ratio",
    }
    for src, dst in alias.items():
        if src in f:
            add_numeric(row, dst, f.get(src))

    # Ports: exact special ports and general top port count/ratio.
    total_packets = float(f.get("total_packets", 0) or 0) or 1.0
    for p, c in flatten_pairs(f.get("port_counts_top", [])):
        try:
            port = int(p)
        except Exception:
            continue
        if port in SPECIAL_PORTS or port in STANDARD_TLS_PORTS or port in {53, 80, 22, 3306, 500, 4500}:
            row[f"port_exact_{port}_count"] = c
            row[f"port_exact_{port}_ratio"] = c / total_packets
    row["special_port_unique_count"] = sum(1 for p, _ in flatten_pairs(f.get("port_counts_top", [])) if _safe_int(p) in SPECIAL_PORTS)

    # Domains / SNI / DNS identity and DNS behavior.
    domains = pairs_to_names(f.get("sni_top", [])) + pairs_to_names(f.get("dns_top", []))
    row["domain_unique_count"] = len(set(domains))
    row["sni_unique_count"] = len(set(pairs_to_names(f.get("sni_top", []))))
    row["dns_unique_count"] = len(set(pairs_to_names(f.get("dns_top", []))))
    for kw in DOMAIN_KEYWORDS:
        safe = re.sub(r"[^a-zA-Z0-9_]+", "_", kw.lower()).strip("_")[:40]
        row[f"domain_kw_{safe}"] = int(any(kw.lower() in d for d in domains))
    for tld in RISK_TLDS:
        safe = tld.strip(".").replace(".", "_")
        row[f"dns_tld_{safe}_present"] = int(any(d.endswith(tld) for d in domains))

    # JA4 / TLS features.
    ja4_list = pairs_to_names(f.get("ja4_top", []))
    row["ja4_unique_count"] = len(set(ja4_list))
    cipher_counts = [extract_ja4_cipher_count(j) for j in ja4_list]
    cipher_counts = [c for c in cipher_counts if c is not None]
    row["ja4_cipher_count_max"] = max(cipher_counts) if cipher_counts else 0
    row["ja4_cipher_count_min"] = min(cipher_counts) if cipher_counts else 0
    row["ja4_has_t13d19"] = int(any(j.startswith("t13d19") for j in ja4_list))
    row["ja4_has_t13d17"] = int(any(j.startswith("t13d17") for j in ja4_list))
    row["ja4_has_t10"] = int(any(j.startswith("t10") for j in ja4_list))
    row["ja4_has_chrome_like"] = int(any(re.match(r"^t13d151[0-9]h", j) for j in ja4_list))
    row["ja4_has_go_like"] = int(any(re.match(r"^t13d1011h2", j) for j in ja4_list))

    # Block ratios with group aliases.
    for n in BLOCK_SIZES:
        row[f"block_{n}_ratio"] = float(f.get(f"block_{n}_ratio", 0) or 0)
    best_block = 0
    best_ratio = 0.0
    for n in BLOCK_SIZES:
        r = row[f"block_{n}_ratio"]
        if r > best_ratio:
            best_block, best_ratio = n, r
    row["block_best_size"] = best_block
    row["block_best_ratio"] = best_ratio

    # Top-flow aggregates.
    if flows:
        row["flow_top_count_exported"] = len(flows)
        row["flow_top_max_duration"] = max(float(x.get("duration", 0) or 0) for x in flows)
        row["flow_top_max_iat_under_1ms_ratio"] = max(float(x.get("iat_under_1ms_ratio", 0) or 0) for x in flows)
        row["flow_top_max_mtu_fill_ratio"] = max(float(x.get("mtu_fill_ratio", 0) or 0) for x in flows)
        row["flow_top_max_ul_dl_ratio"] = _finite_max([x.get("ul_dl_ratio", 0) for x in flows])
    else:
        row["flow_top_count_exported"] = 0

    # Protocol flags.
    prot = " ".join(str(k).lower() for k in (f.get("protocol_counts", {}) or {}).keys())
    for name in ["tls", "quic", "wg", "isakmp", "ike", "esp", "http", "dns"]:
        row[f"protocol_has_{name}"] = int(name in prot)

    return row


def _safe_int(x: Any) -> int:
    try:
        return int(float(str(x)))
    except Exception:
        return 0


def _finite_max(values: Iterable[Any]) -> float:
    out = 0.0
    for v in values:
        try:
            x = float(v)
        except Exception:
            continue
        if math.isfinite(x):
            out = max(out, x)
    return out


def build_dataset(
    features_jsonl: Path,
    labels_path: Optional[Path],
    out_path: Path,
    csv_out: Optional[Path] = None,
    filter_unlabeled: bool = True,
    feature_version: str = "v2_flatten",
    label_version: str = "labels_master_v1",
) -> pd.DataFrame:
    """Build a strict ML dataset from feature records and optional labels."""

    labels = read_labels(labels_path) if labels_path else pd.DataFrame()
    rows: List[Dict[str, Any]] = []
    for rec in load_jsonl(features_jsonl):
        row = flatten_feature_record(rec)
        row.update(merge_label(row, labels))
        row["feature_version"] = feature_version
        row["label_version"] = label_version
        rows.append(row)
    df = pd.DataFrame(rows)

    # Normalize binary label where possible.
    if "label_binary" in df.columns:
        df["label_binary"] = df["label_binary"].apply(lambda x: None if pd.isna(x) or str(x).strip() == "" else _normalize_binary_label(x))
    if "label_confidence" not in df.columns:
        df["label_confidence"] = "unlabeled"
    df["label_confidence"] = df["label_confidence"].map(normalize_confidence)
    df["sample_weight"] = df["label_confidence"].map(sample_weight_for_confidence)
    if filter_unlabeled:
        before = len(df)
        df = df[df["label_confidence"] != "unlabeled"].copy()
        logger.info("Filtered unlabeled rows: %d -> %d", before, len(df))

    # Put metadata first.
    ordered = [c for c in [
        "sample_id", "capture_id", "split_group", "source_archive", "pcap_member", "file_name",
        "label_binary", "label_protocol", "label_tool", "label_family", "scenario",
        "label_confidence", "sample_weight", "review_status", "label_status",
        "feature_version", "label_version", "need_review", "note",
    ] if c in df.columns]
    rest = [c for c in df.columns if c not in ordered]
    df = df[ordered + sorted(rest)]

    out_path.parent.mkdir(parents=True, exist_ok=True)
    if out_path.suffix.lower() == ".parquet":
        df.to_parquet(out_path, index=False)
    else:
        df.to_csv(out_path, index=False, encoding="utf-8-sig")
    if csv_out:
        csv_out.parent.mkdir(parents=True, exist_ok=True)
        df.to_csv(csv_out, index=False, encoding="utf-8-sig")
    logger.info("Wrote dataset: %s (%d rows, %d columns)", out_path, len(df), len(df.columns))
    return df


def feature_columns(df: pd.DataFrame, feature_set: str = "all") -> List[str]:
    candidates = [c for c in df.columns if c not in META_COLUMNS]
    numeric = [c for c in candidates if pd.api.types.is_numeric_dtype(df[c])]
    if feature_set == "all":
        return numeric
    if feature_set == "no_identity":
        return [
            c for c in numeric
            if not c.startswith(IDENTITY_PREFIXES)
            and c not in IDENTITY_EXACT_COLUMNS
            and not any(token in c.lower() for token in ("sni", "domain_kw", "dns_tld", "ip_", "asn_", "endpoint_ip"))
        ]
    if feature_set == "behavior_only":
        return [c for c in numeric if c.startswith(BEHAVIOR_PREFIXES)]
    if feature_set == "tls_only":
        return [c for c in numeric if c.startswith(TLS_PREFIXES)]
    if feature_set == "dns_only":
        return [c for c in numeric if c.startswith(DNS_PREFIXES)]
    if feature_set == "port_only":
        return [c for c in numeric if c.startswith(PORT_PREFIXES)]
    raise ValueError(f"Unknown feature_set: {feature_set}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Build ML-ready dataset from features.jsonl + labels/manifest")
    parser.add_argument("--features", required=True, help="features.jsonl path")
    parser.add_argument("--labels", help="labels.csv/xlsx path")
    parser.add_argument("--out", default="datasets/ml_dataset.parquet", help="output .csv or .parquet")
    parser.add_argument("--csv-out", help="optional CSV copy")
    parser.add_argument("--include-unlabeled", action="store_true", help="keep unlabeled rows with sample_weight=0")
    parser.add_argument("--feature-version", default="v2_flatten")
    parser.add_argument("--label-version", default="labels_master_v1")
    args = parser.parse_args()
    logging.basicConfig(level=logging.INFO, format="[%(levelname)s] %(message)s")
    build_dataset(
        Path(args.features),
        Path(args.labels) if args.labels else None,
        Path(args.out),
        csv_out=Path(args.csv_out) if args.csv_out else None,
        filter_unlabeled=not args.include_unlabeled,
        feature_version=args.feature_version,
        label_version=args.label_version,
    )


if __name__ == "__main__":
    main()
